 212 323 434 545 656 767 878 989
 121 232 343 454 565 676 787 898
 123 234 345 456 567 678 789
 
 N(2) * 2 - (Int.last 9|0).count
 
 
 1010 //// 3210 4321 5432 6543 7654 8765 9876
 //// 2101 3212 4323 5434 6545 7656 8767 9878
 //// 2121 3232 4343 5454 6565 7676 8787 9898
 //// 2123 3234 3234 4345 5456 6567 7678 8789
 1212 2323 3434 4545 5656 6767 7878 8989 ////
 1210 2321 3432 4543 5654 6765 7876 8987 ////
 1234 2345 3456 4567 5678 6789 //// //// ////
