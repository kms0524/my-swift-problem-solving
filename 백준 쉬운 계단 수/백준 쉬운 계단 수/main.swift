 dp[1][1] = 1
 
 dp[2][1] = 1
 
 dp[3][1] = 3
 dp[3][3] = dp[2][2] + dp[2][4] = 2 + 2 = 4 = 4
 
 
 dp[4][1] = 3
 */


var dp = Array(repeating: Array(repeating: 0, count: 10), count: 1001)

for i in 1...9 {
    dp[1][i] = 1
}

for i in 0...9 {
    if i == 0 || i == 1 || i == 9 {
        dp[2][i] = 1
    }
    else {
        dp[2][i] = 2
    }
}

if N < 3 {
    print(dp[N].reduce(0){ $0 + $1 })
}
else {
    
    for i in 3...N {
        for j in 0...9 {
            if j == 0 {
                dp[i][j] = dp[i-1][j+1] % 1000000000
            }
            else if j == 9 {
                dp[i][j] = dp[i-1][j-1] % 1000000000
            }
            else {
                dp[i][j] = (dp[i-1][j-1] + dp[i-1][j+1]) % 1000000000
            }
        }
        
    }
    
    print(dp[N].reduce(0){ $0 + $1 } % 1000000000)
}
